create policy "Allow anon read recipes" on recipes for select using (true);
create policy "Allow anon insert recipes" on recipes for insert with check (true);
create policy "Allow anon read recipe_lines" on recipe_lines for select using (true);
create policy "Allow anon insert recipe_lines" on recipe_lines for insert with check (true);
create policy "Allow anon read batches" on batches for select using (true);
create policy "Allow anon insert batches" on batches for insert with check (true);
create policy "Allow anon read finished_goods" on finished_goods for select using (true);
create policy "Allow anon insert finished_goods" on finished_goods for insert with check (true);
create policy "Allow anon read sales" on sales for select using (true);
create policy "Allow anon insert sales" on sales for insert with check (true);
